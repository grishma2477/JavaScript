// rules for making variables

// you cannot start with number
// 1value
// value1

var value1 = 10;
console.log(value1 ** 2)

// you can use only underscore _ or dollar symbol
// first_name (valid)
// _firstname (valid)

var _firstname = "Grishma"
console.log(_firstname)
// first$name (valid)
//  $firstname (valid)

// you cannot use spaces
var first_name = "Grishma" // snake case writing
var firstName = "Grishma" // camel case writing 
// first name (invalid)

